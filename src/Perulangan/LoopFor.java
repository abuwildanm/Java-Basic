package Perulangan;

public class LoopFor {

    public static void main(String[] args) {
        int nilai;

        for (nilai = 1; nilai <= 10; nilai++)
        {
            System.out.println(nilai);
        }
    }
    
}

//InitializationExpression : inisialisasi dari variabel loop (memberikan nilai awal loop)
//InitializationExpression : nilai = 1

//LoopCondition : Membandingkan variabel loop pada nilai batas tertentu
//LoopCondition : nilai <= 10

//StepExpression : Melakukan update pada variabel loop
//StepExpression : nilai++

